package com.example.beesmart.data.local.entity

import androidx.room.Entity
import androidx.room.PrimaryKey
import com.example.beesmart.network.models.InspectionResponse

@Entity(tableName = "inspections")
data class InspectionEntity(
    @PrimaryKey val localId: String,
    val serverId: String?,
    val hiveLocalId: String,
    val hiveServerId: String?,
    val hiveName: String,
    val apiaryId: String,
    val apiaryName: String,
    val inspectionDate: String,
    val temperature: Double?,
    val framesCount: Int?,
    val broodFrames: Int?,
    val honeyFrames: Int?,
    val pollenFrames: Int?,
    val queenSeen: Boolean,
    val eggsSeen: Boolean,
    val larvaeSeen: Boolean,
    val photosCount: Int,
    val createdAt: String,
    val syncStatus: SyncStatus = SyncStatus.SYNCED,
    val updatedAt: Long = System.currentTimeMillis(),
    val queenCellsSeen: Boolean = false,
    val queenCellsWithEggs: Boolean = false,
    val beardingAtEntrance: Boolean = false,
    val spaceNeeded: Boolean = false,
    val broodPattern: String? = null,
    val honeyCappingPercent: Int? = null,
    val feedingGiven: Boolean = false,
    val waterAvailable: Boolean = false,
    val moistureOrMold: Boolean = false,
    val deadBeesAtEntrance: Boolean = false,
    val unusualBehavior: Boolean = false,
    val temperament: String? = null,
    val oldCombsToReplace: Int? = null,
    val notes: String? = null
) {
    fun toInspectionResponse() = InspectionResponse(
        id = serverId ?: localId,
        hiveId = hiveServerId ?: hiveLocalId,
        hiveName = hiveName,
        apiaryId = apiaryId,
        apiaryName = apiaryName,
        inspectionDate = inspectionDate,
        temperature = temperature,
        framesCount = framesCount,
        broodFrames = broodFrames,
        honeyFrames = honeyFrames,
        pollenFrames = pollenFrames,
        queenSeen = queenSeen,
        eggsSeen = eggsSeen,
        larvaeSeen = larvaeSeen,
        photosCount = photosCount,
        createdAt = createdAt,
        updatedAt = updatedAt.toString(),
        queenCellsSeen = queenCellsSeen,
        queenCellsWithEggs = queenCellsWithEggs,
        beardingAtEntrance = beardingAtEntrance,
        spaceNeeded = spaceNeeded,
        broodPattern = broodPattern,
        honeyCappingPercent = honeyCappingPercent,
        feedingGiven = feedingGiven,
        waterAvailable = waterAvailable,
        moistureOrMold = moistureOrMold,
        deadBeesAtEntrance = deadBeesAtEntrance,
        unusualBehavior = unusualBehavior,
        temperament = temperament,
        oldCombsToReplace = oldCombsToReplace,
        notes = notes
    )
}

fun InspectionResponse.toEntity() = InspectionEntity(
    localId = id,
    serverId = id,
    hiveLocalId = hiveId,
    hiveServerId = hiveId,
    hiveName = hiveName,
    apiaryId = apiaryId,
    apiaryName = apiaryName,
    inspectionDate = inspectionDate,
    temperature = temperature,
    framesCount = framesCount,
    broodFrames = broodFrames,
    honeyFrames = honeyFrames,
    pollenFrames = pollenFrames,
    queenSeen = queenSeen,
    eggsSeen = eggsSeen,
    larvaeSeen = larvaeSeen,
    photosCount = photosCount,
    createdAt = createdAt,
    syncStatus = SyncStatus.SYNCED,
    updatedAt = System.currentTimeMillis(),
    queenCellsSeen = queenCellsSeen,
    queenCellsWithEggs = queenCellsWithEggs,
    beardingAtEntrance = beardingAtEntrance,
    spaceNeeded = spaceNeeded,
    broodPattern = broodPattern,
    honeyCappingPercent = honeyCappingPercent,
    feedingGiven = feedingGiven,
    waterAvailable = waterAvailable,
    moistureOrMold = moistureOrMold,
    deadBeesAtEntrance = deadBeesAtEntrance,
    unusualBehavior = unusualBehavior,
    temperament = temperament,
    oldCombsToReplace = oldCombsToReplace,
    notes = notes
)
